#loop com for
# sintaxe
# for var de controle in range(n) (n irá de zero até n-1)

print("range com apenas um parâmetro (10):")
for i in range(11):
    print("valor do i:", i)

print("\n")
print("range com 2 parâmetros (2, 8)")
for i in range(2, 8):
    print("valor do i:", i) # dois parâmetro n, m faz o i ir de n até m-1

print("\n")
print("range com 3 parâmetros (2, 8, 3)")
for i in range(2, 8, 3):
    print("valor de i:", i)

    

 

